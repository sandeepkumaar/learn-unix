#!/bin/sh
# This is a comment!
echo "Hello      World"	      # This is a comment, too!
echo "Hello World"
echo "Hello * World"
echo Hello * World
# 3 args, '*' that represents all the contents in that directory
echo Hello      World
echo "Hello" World
echo Hello "     " World
# 3 args
echo "Hello \"*\" World"
echo `hello` world
# `hello` is executed on the terminal
# this is useful when you interpolate 
echo 'hello' world
